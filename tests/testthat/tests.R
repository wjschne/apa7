library(apa7)
library(testthat)
test_that("apa_p", {
  expect_equal(apa_p(0), "*p*&nbsp;<&nbsp;.001")
  expect_equal(apa_p(0, FALSE), "<.001")
  expect_equal(apa_p(0.0007), "*p*&nbsp;<&nbsp;.001")
  expect_equal(apa_p(0.001), "*p*&nbsp;=&nbsp;.001")
  expect_equal(apa_p(0.000999), "*p*&nbsp;<&nbsp;.001")
  expect_equal(apa_p(0.000999, FALSE), "<.001")
  expect_equal(apa_p(0.001), "*p*&nbsp;=&nbsp;.001")
  expect_equal(apa_p(0.005, FALSE), ".005")
  expect_equal(apa_p(0.0081), "*p*&nbsp;=&nbsp;.008")
  expect_equal(apa_p(0.00999, FALSE), ".01")
  expect_equal(apa_p(0.00999), "*p*&nbsp;=&nbsp;.01")
  expect_equal(apa_p(0.00999, FALSE), ".01")
  expect_equal(apa_p(0.0999), "*p*&nbsp;=&nbsp;.10")
  expect_equal(apa_p(0.0999, FALSE), ".10")
  expect_equal(apa_p(0.0999), "*p*&nbsp;=&nbsp;.10")
  expect_equal(apa_p(0.0999, FALSE), ".10")
  expect_equal(apa_p(0.0999, plain = T), "p = .10")
  expect_equal(apa_p(0, plain = T), "p < .001")
})
